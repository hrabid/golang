package main

import (
	"fmt"
)

func main() {
	/* Operators 
	* * Arithmetic Operators
	* * Assignment Operators
	* * Comparison Operators
	* * Logical Operators
	* * Bitwise Operators
	*/

	var s int = 12 + 48
	fmt.Println(s)

	var n1 int = 45
	var sum1 int = n1 + 45
	var sum2 int = sum1 + 45
	var sum3 int = sum1 + sum2
	fmt.Println(sum1)
	fmt.Println(sum2)
	fmt.Println(sum3)
}
